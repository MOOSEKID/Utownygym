export const get = ({ filter }) => {
  return new Promise((resolve, reject) => {
    try {
      const needle = filter.toLowerCase();
      const data = countries.filter(
        ({ name }) => name.toLowerCase().indexOf(needle) > -1
      );
      resolve({ data, meta: { current_page: 1, last_page: 1 } });
    } catch (error) {
      reject(error);
    }
  });
};

export const getCurrencySymbol = (val) => {
  return new Promise((resolve, reject) => {
    try {
      const needle = val.toLowerCase();
      const data = countries.find((item) => item.currency === needle);
      resolve(data);
    } catch (error) {
      reject(error);
    }
  });
};

